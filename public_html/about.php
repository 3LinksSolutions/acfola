            <p>Formed on November 25, 2009, the African Canadian Federation of London and Area (ACFOLA) is a
                non-profit
                organization based in London, Ontario. ACFOLA is an umbrella organization of African Canadian
                associations and individuals. The organization sees its role as encouraging its members to
                participate
                and be included in economic, social and cultural development. Its strategic direction is to
                contribute
                to developing London, Ontario and surrounding areas as a socially and economically prosperous region
                in
                which no one is left behind.</p>

            <p>Strategically, ACFOLA is focused on:
            <ul>
                <li>working with African community associations and individuals;</li>
                <li>helping African Canadians to be involved and be part of their local communities;</li>
                <li>empowering African Canadians through cultural awareness, health, education and employment; and
                </li>
                <li>pulling together the strengths of ACFOLA’s members, friends and associates.</li>
            </ul>
            </p><p>We believe our activities will result in stronger African Canadian communities and families,
                which
                will translate into increased cultural awareness and civic responsibility that will enhance the
                social and
                economic advancement of African Canadian individuals and communities – to improve the vitality of
                our
                communities and cities. ACFOLA has a shared mission with other African organizations, and it works
                closely
                with these organizations to enhance and expand access to quality, community-responsive social
                programs for
                all African Canadians who are under-served and underrepresented. We also strive towards the
                inclusion,
                engagement and integration of everyone in community life and development of London and area. Through
                its
                private and public partnerships and sponsorship, ACFOLA assists by identifying and addressing the
                needs and
                aspirations of African Canadians in London, Ontario and surrounding areas.
            </p>

            <h3>Mission Statement</h3>
            <p>To unite African-Canadian individuals and organizations through collaborative efforts that promote
                inclusion, integration and engagement in London and area; and to promote our individual, group and
                community welfare regardless of race, religion, ethnicity, sexual orientation or gender.</p>

